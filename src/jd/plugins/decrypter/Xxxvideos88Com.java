package jd.plugins.decrypter;

import java.util.ArrayList;

import jd.PluginWrapper;
import jd.controlling.ProgressController;
import jd.nutils.encoding.Encoding;
import jd.plugins.CryptedLink;
import jd.plugins.DecrypterPlugin;
import jd.plugins.DownloadLink;
import jd.plugins.PluginForDecrypt;

@DecrypterPlugin(revision = "$Revision$", interfaceVersion = 3, names = { "xxxvideos88.com" }, urls = { "https?://(?:www\\.)?xxxvideos88\\.com/.+" }, flags = { 0 })
public class Xxxvideos88Com extends PluginForDecrypt {

    public Xxxvideos88Com(PluginWrapper wrapper) {
        super(wrapper);
    }

    public ArrayList<DownloadLink> decryptIt(CryptedLink param, ProgressController progress) throws Exception {
        ArrayList<DownloadLink> decryptedLinks = new ArrayList<DownloadLink>();
        final String parameter = param.toString();
        this.br.setFollowRedirects(true);
        br.getPage(parameter);
        if (br.getHttpConnection().getResponseCode() == 404) {
            decryptedLinks.add(this.createOfflinelink(parameter));
            return decryptedLinks;
        }
        final String b64 = this.br.getRegex("source:[\t\n\r ]*?\\'([^<>\"\\'/]+)\\'").getMatch(0);
        if (b64 != null) {
            decryptedLinks.add(createDownloadlink(Encoding.Base64Decode(b64)));
        }

        return decryptedLinks;
    }

}
