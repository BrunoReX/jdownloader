    public static String getFilename(final String correctedBR) {
        // this is presented in /f/uid and standard url
        final Regex fnameregex = new Regex(correctedBR, patternDownloadFile);
        String filename = new Regex(correctedBR, "You have requested.*?https?://(www\\.)?(?:sharesix|filenuke)\\.com/[A-Za-z0-9]{12}/(.*?)</font>").getMatch(1);
        if (filename == null) {
            filename = fnameregex.getMatch(0);
        }
        if (filename == null) {
            filename = new Regex(correctedBR, ">Download File ([^<>\"]*?) \\([^<>\\(\\)]+\\)<").getMatch(0);
        }
        if (filename == null) {
            filename = new Regex(correctedBR, ">Download File(.*?)</").getMatch(0);
            if (filename == null) {
                // generic regex will pick up false positives (html)
                // adjust to make work with COOKIE_HOST
                filename = new Regex(correctedBR, "(?i)Filename: ?(<[^>]+> ?)+?([^<>\"\\']+)").getMatch(1);
            }
        }
        /* @ in filename triggers email protection */
        final String dl_contains_at = new Regex(correctedBR, flname_pt1 + "<a class=\".*?</p>").getMatch(-1);
        if (filename == null && dl_contains_at != null) {
            filename = jd.plugins.hoster.antiDDoSForHost.getStringFromCloudFlareEmailProtection(dl_contains_at);
        }
        if (filename != null) {
            filename = Encoding.htmlDecode(filename);
            filename = filename.trim();
            filename = filename.replaceAll("(</b>|<b>|\\.html)", "");
        }
        return filename;
    }

    public static String getFilesize(final String correctedBR) {
        // this is presented in /f/uid and standard url
        final Regex fnameregex = new Regex(correctedBR, patternDownloadFile);
        String filesize = fnameregex.getMatch(1);
        if (filesize == null) {
            filesize = new Regex(correctedBR, "\\(([0-9]+ b(?:ytes)?)\\)").getMatch(0);
        }
        if (filesize == null) {
            filesize = new Regex(correctedBR, "</font>[ ]+\\(([^<>\"\\'/]+)\\)(.*?)</font>").getMatch(0);
        }
        if (filesize == null) {
            /* More generic RegEx - this will work in most|all cases */
            filesize = new Regex(correctedBR, "(\\d+(\\.\\d+)? ?(?:KB|MB|GB))").getMatch(0);
        }
        if (filesize == null) {
            /* Last chance - allow 'b(ytes)' --> High chances to pick up false positives as hashes often contain '0b' or similar */
            filesize = new Regex(correctedBR, "(\\d+(\\.\\d+)? ?(?:B(?:ytes?)?))").getMatch(0);
        }
        return filesize;
    }

